// Función para obtener el id de la URL
function obtenerIdDeURL() {
    const params = new URLSearchParams(window.location.search);
    return params.get('id');
}

// Función para cargar los detalles de la noticia
function cargarNoticia() {
    const noticiaId = obtenerIdDeURL(); // Obtiene el id desde la URL
    if (!noticiaId) {
        console.error('No se proporcionó un id en la URL');
        return;
    }

    const noticia = noticias.find(noticia => noticia.id === parseInt(noticiaId));

    if (noticia) {
        // Obtener el contenedor de detalles de la noticia
        const contenedor = document.getElementById('noticia-detalle');

        // Crear el contenido HTML de la noticia
        const contenidoHTML = `
            <section>
                <h2>${noticia.titulo}</h2>
                <div class="categoria">${noticia.categoria.charAt(0).toUpperCase() + noticia.categoria.slice(1)}</div>
                <img src="${noticia.imagen}" alt="Imagen noticia ${noticia.id}" class="news-img">
                <div class="contenido">
                    ${noticia.contenido_completo}
                </div>
            </section>
        `;

        // Inyectar el contenido en el contenedor
        contenedor.innerHTML = contenidoHTML;
    } else {
        console.error('Noticia no encontrada');
        const contenedor = document.getElementById('noticia-detalle');
        contenedor.innerHTML = '<p>La noticia solicitada no fue encontrada.</p>';
    }
}

// Llamar a la función cuando el DOM esté listo
document.addEventListener('DOMContentLoaded', cargarNoticia);
